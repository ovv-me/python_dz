from itertools import count, cycle, islice


def my_func1(start_numb, stop_numb):
    for el in islice(count(start_numb), ((stop_numb+1)-start_numb)):
        print(el, end=' ')


imp1 = int(input("Введите стартовое число: "))
imp2 = int(input("Введите стоп число: "))
my_func1(imp1, imp2)

