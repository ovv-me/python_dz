# from itertools import cycle

my_list = [5, 3, 3, 1, 0, 4, 2, 4, 7, 3]


def my_func2(stop):
    i = 1
    for value in cycle(my_list):
        if i > stop:
            break
        print(value, end=' ')
        i += 1


my_func2(stop=int(input('Введите ограничитель')))
